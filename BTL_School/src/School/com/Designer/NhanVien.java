    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
    PhongdaotaoModal pdtModal = new PhongdaotaoModal();
    pdtModal.setMaNV(txtmanv.getText());
    pdtModal.setHoten(txttennv.getText());

    PhongDaoTaoDAO phongDaoTaoDao = new PhongDaoTaoDAO();
    int kq = phongDaoTaoDao.update(pdtModal);
    hienThi(); 
     if (kq > 0) {
        JOptionPane.showMessageDialog(this, "Sửa thành công!","thông báo",JOptionPane.QUESTION_MESSAGE);
        } else {
        JOptionPane.showMessageDialog(this, "Sửa thất bại!","thông báo",JOptionPane.QUESTION_MESSAGE);
    }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
       String maNV = txtmanv.getText();

        PhongDaoTaoDAO phongDaoTaoDao = new PhongDaoTaoDAO();
        int kq = phongDaoTaoDao.delete(maNV);
        hienThi();
         if (kq > 0) {
        JOptionPane.showMessageDialog(this, "Xóa thành công!","thông báo",JOptionPane.QUESTION_MESSAGE);
        } else {
        JOptionPane.showMessageDialog(this, "Xóa thất bại!","thông báo",JOptionPane.QUESTION_MESSAGE);
    }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void tbnhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbnhanvienMouseClicked
           int selectedRow = tbnhanvien.getSelectedRow();
    if (selectedRow >= 0) {
        // Lấy dữ liệu từ các cột của hàng được chọn
        String maKhoa = tbnhanvien.getValueAt(selectedRow, 0).toString();
        String tenKhoa = tbnhanvien.getValueAt(selectedRow, 1).toString();
        
        // Điền dữ liệu vào các trường văn bản
        txtmanv.setText(maKhoa);
        txttennv.setText(tenKhoa);
    }
    }//GEN-LAST:event_tbnhanvienMouseClicked

