    private void hienThi(){
        PhongDaoTaoDAO pdtdao = new PhongDaoTaoDAO();
    ArrayList<PhongdaotaoModal> lst = pdtdao.getList();
    
    // Tạo DefaultTableModel với tiêu đề các cột
    DefaultTableModel model = new DefaultTableModel(new String[]{"Mã Nhân Viên", "Tên Nhân Viên"}, 0);
    
    // Thêm các đối tượng Khoa vào model
    for (PhongdaotaoModal  PDTModal: lst) {
        model.addRow(new Object[]{PDTModal.getMaNV(), PDTModal.getHoten()});
    }
    
    // Gán model cho bảng tblKhoa
    tbnhanvien.setModel(model);
    }
    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        PhongdaotaoModal pdtModal=new PhongdaotaoModal();
        pdtModal.setMaNV(txtmanv.getText());
        pdtModal.setHoten(txttennv.getText());
        PhongDaoTaoDAO pdtdao=new PhongDaoTaoDAO();
        int kq=pdtdao.add(pdtModal);
        hienThi();
        
        if (kq > 0) {
        JOptionPane.showMessageDialog(this, "Thêm thành công!","thông báo",JOptionPane.QUESTION_MESSAGE);
        } else {
        JOptionPane.showMessageDialog(this, "Thêm thất bại!","thông báo",JOptionPane.QUESTION_MESSAGE);
    }
    }//GEN-LAST:event_btnthemActionPerformed

